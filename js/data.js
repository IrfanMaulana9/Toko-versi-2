  // Global helper functions
  window.getProducts = () => window.PRODUCTS_DATA || []
  
  window.getProductById = (id) => {
    const products = window.getProducts()
    return products.find((product) => product.id === Number.parseInt(id))
  }
  
  window.getProductsByCategory = (category) => {
    const products = window.getProducts()
    return products.filter((product) => product.category === category)
  }
  
  window.getFeaturedProducts = (limit = 4) => {
    const products = window.getProducts()
    return products.slice(0, limit)
  }
  
  window.searchProducts = (query) => {
    const products = window.getProducts()
    const lowercaseQuery = query.toLowerCase()
    return products.filter(
      (product) =>
        product.name.toLowerCase().includes(lowercaseQuery) ||
        product.description.toLowerCase().includes(lowercaseQuery) ||
        product.brand.toLowerCase().includes(lowercaseQuery) ||
        product.category.toLowerCase().includes(lowercaseQuery),
    )
  }
  
  window.filterProducts = (filters) => {
    let products = window.getProducts()
  
    // Filter by category
    if (filters.categories && filters.categories.length > 0) {
      products = products.filter((product) => filters.categories.includes(product.category))
    }
  
    // Filter by price range
    if (filters.minPrice) {
      products = products.filter((product) => product.price >= filters.minPrice)
    }
    if (filters.maxPrice) {
      products = products.filter((product) => product.price <= filters.maxPrice)
    }
  
    // Filter by rating
    if (filters.rating) {
      products = products.filter((product) => product.rating >= filters.rating)
    }
  
    return products
  }
  
  window.sortProducts = (products, sortBy) => {
    const sortedProducts = [...products]
  
    switch (sortBy) {
      case "price-low":
        return sortedProducts.sort((a, b) => a.price - b.price)
      case "price-high":
        return sortedProducts.sort((a, b) => b.price - a.price)
      case "rating":
        return sortedProducts.sort((a, b) => b.rating - a.rating)
      case "popular":
        return sortedProducts.sort((a, b) => b.reviews - a.reviews)
      case "discount":
        return sortedProducts.sort((a, b) => b.discount - a.discount)
      case "newest":
      default:
        return sortedProducts
    }
  }
  
